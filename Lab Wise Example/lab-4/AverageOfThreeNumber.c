/*2. Calculate average of three numbers.*/
#include<stdio.h>

int main()
{
  float a , b , c ;
  printf("Enter a Number:\n");
  scanf("%f",&a);
  printf("Enter b Number:\n");
  scanf("%f",&b);
  printf("Enter c Number:\n");
  scanf("%f",&c);
  printf("Average of Three Number is :%f",(a+b+c)/3.0);
  return 0;
}